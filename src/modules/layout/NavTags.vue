<template>
    <section class="layout__tags">
        <div class="navTags">
            <div class="navTags__btn navTags__btn--left" :class="{ 'navTags__btn--disabled': !moveAble }" @click="move(-1)">
                <i class="el-icon-caret-left"></i>
            </div>
            <div class="navTags__box swiper-container">
                <div class="navTags__container swiper-wrapper">
                    <el-tag
                        @contextmenu.prevent.native="openMenu(item, $event)"
                        @click="$router.push({ name: item.name })"
                        :class="{ isActive: isTagActive(item.name) }"
                        :closable="item.name !== 'platform'"
                        @close="closeTag(item)"
                        size="medium"
                        class="swiper-slide"
                        v-for="item in navTags"
                        :key="item.name"
                    >
                        <span class="dotted"></span>
                        <span>{{ item.title }}</span>
                    </el-tag>
                </div>
            </div>
            <div class="navTags__btn navTags__btn--right" :class="{ 'navTags__btn--disabled': !moveAble }" @click="move(1)">
                <i class="el-icon-caret-right"></i>
            </div>
            <el-dropdown @command="handleTag">
                <div class="navTags__btn">
                    <i class="el-icon-circle-close"></i>
                </div>
                <el-dropdown-menu slot="dropdown">
                    <el-dropdown-item command="closeAll">关闭所有</el-dropdown-item>
                    <el-dropdown-item command="closeOthers">关闭其他</el-dropdown-item>
                </el-dropdown-menu>
            </el-dropdown>
        </div>
        <ul class="contextmenu" v-show="visible" :style="{ left: left + 'px', top: top + 'px' }">
            <li @click="closeSelectedTag(selectedTag)">关闭</li>
            <li @click="closeOthersTags">关闭其他</li>
            <li @click="closeAllTags">关闭所有</li>
        </ul>
    </section>
</template>

<script>
import { mapGetters, mapMutations } from 'vuex'
import Swiper from 'swiper'
export default {
    name: 'navTags',
    data() {
        return {
            visible: false,
            top: 0,
            left: 0,
            selectedTag: {},
            swiper: null,
            moveAble: true
        }
    },
    watch: {
        $route: {
            handler(route) {
                this.addTag(route)
            },
            immediate: true
        },
        visible(value) {
            if (value) {
                document.body.addEventListener('click', this.closeMenu)
            } else {
                document.body.removeEventListener('click', this.closeMenu)
            }
        }
    },
    computed: {
        ...mapGetters(['navTags']),
        activeTag() {
            const { matched } = this.$route
            let tagName, tagTitle
            for (let i = matched.length - 1; i >= 0; i--) {
                const { name, meta } = matched[i]
                if (meta.hidden === 'TRUE') {
                    continue
                } else {
                    tagName = name
                    tagTitle = meta.title
                    break
                }
            }
            return { tagName, tagTitle }
        }
    },
    methods: {
        ...mapMutations(['ADD_NAVTAGS', 'DEL_NAVTAGS', 'DEL_OTHER_NAVTAGS', 'DEL_ALL_NAVTAGS']),
        initSwiper() {
            this.swiper = new Swiper('.swiper-container', {
                slidesPerView: 'auto',
                loop: false,
                mousewheel: true,
                roundLengths: true,
                observer: true
            })
        },
        move(flag) {
            flag > 0 ? this.swiper.slideNext() : this.swiper.slidePrev()
        },
        handleTag(command) {
            if (command === 'closeOthers') {
                this.selectedTag = this.navTags.find(v => v.name === this.$route.name)
                this.closeOthersTags()
            } else {
                this.closeAllTags()
            }
        },
        isTagActive(tagName) {
            return tagName === this.activeTag.tagName
        },
        // 添加标签
        addTag(route) {
            const { meta, matched, name: rName } = route
            if (!rName) return
            const { tagName: name, tagTitle: title } = this.activeTag
            this.ADD_NAVTAGS({ name, title })
            this.$nextTick(() => {
                let index = [...document.querySelectorAll('.swiper-wrapper .el-tag')].findIndex(item => item.classList.contains('isActive'))
                this.swiper.slideTo(index)
            })
        },
        // 删除标签
        closeTag(tag) {
            // 控制路由重新跳转
            const { tagName } = this.activeTag
            if (this.navTags.length === 1) {
                this.$router.push({ name: 'platform' })
            } else if (tagName === tag.name) {
                for (const [k, v] of this.navTags.entries()) {
                    if (v.name === tag.name) {
                        if (k !== this.navTags.length - 1) {
                            this.$router.push(this.navTags[k + 1])
                        } else if (k !== 0) {
                            this.$router.push(this.navTags[k - 1])
                        }
                        break
                    }
                }
            }
            // 执行删除操作
            this.DEL_NAVTAGS(tag)
        },
        closeSelectedTag(tag) {
            this.closeTag(tag)
            this.visible = false
        },
        closeOthersTags() {
            // 执行删除操作
            this.$router.push({ name: this.selectedTag.name })
            this.DEL_OTHER_NAVTAGS(this.selectedTag)
        },
        closeAllTags() {
            this.DEL_ALL_NAVTAGS()
            this.$router.push('/platform/index')
        },
        openMenu(tag, e) {
            this.visible = true
            this.selectedTag = tag
            this.left = e.clientX
            this.top = e.clientY
        },
        closeMenu() {
            this.visible = false
        }
    },
    mounted() {
        this.initSwiper()
    }
}
</script>
